// 1. 1 to 10 number print 

let i = 1;
while(i<=10){
    console.log(i);
    i++
}


// 2. 10 to 1 number print reverse 

let count = 10;
while(count>=1){
    console.log(count);
    count--
}


// 3.  1 to 20 print even number 

let count1 =1;
while(count1<=20){
    if(count1%2===0){
        console.log(count1)
    }
    count1++
}